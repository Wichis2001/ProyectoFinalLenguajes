/**
 * Este enum se encarga de establecer como constantees los distintos tipos de palabras reservadas que me puedo llegar a encontrar en mis analizadores
 * @author luis
 */
public enum PalabrasReservadas {

    /**
     * Tipo de enum que representa la palabra reservada escribir
     */
    ESCRIBIR,

    /**
     * Tipo de enum que representa la palabra reservada fin
     */
    FIN,

    /**
     * Tipo de enum que representa la palabra reservada repetir
     */
    REPETIR,

    /**
     * Tipo de enum que representa la palabra reservada iniciar
     */
    INICIAR,

    /**
     * Tipo de enum que representa la palabra reservada si
     */
    SI,

    /**
     * Tipo de enum que representa la palabra reservada verdadero
     */
    VERDADERO,

    /**
     * Tipo de enum que representa la palabra reservada falso
     */
    FALSO,

    /**
     * Tipo de enum que representa la palabra reservada entonces
     */
    ENTONCES
}
